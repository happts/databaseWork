package entity;
/**
 * 贾天豪
 */
public class Co {
    private String cno;     //在类当中定义string型变量cno
    private String cname;   //在类当中定义string型变量cname

    public void setCno(String cno) {
        this.cno = cno;
    }//对cno进行赋值

    public String getCno() {
        return cno;
    }  //将cno所获得的值进行返回

    public void setCname(String cname) {
        this.cname = cname;
    }//对cname进行赋值

    public String getCname() {
        return cname;
    }//将cname所获得的值进行返回

}
